--para HECHOSORDENESSERVICIO
 
SELECT o.ID_ORDEN, o.FECHA_RECEPCION, o.ID_CLIENTE, o.ID_VEHICULO, o.ID_EMPLEADO, SUM(d.CANTIDAD) AS CANTIDAD_SERVICIOS, SUM(d.CANTIDAD * s.COSTO) AS COSTO_TOTAL
FROM ORDENES_SERVICIO o
LEFT JOIN DETALLE_SERVICIO d ON o.ID_ORDEN = d.ID_ORDEN 
LEFT JOIN SERVICIOS s ON d.ID_SERVICIO = s.ID_SERVICIO
GROUP BY o.ID_ORDEN, o.FECHA_RECEPCION, o.ID_CLIENTE, o.ID_VEHICULO, o.ID_EMPLEADO;



