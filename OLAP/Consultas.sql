--para DIMTIEMPO
SELECT 
    DISTINCT FECHA_RECEPCION,
    TO_CHAR(FECHA_RECEPCION, 'YYYY') AS año,
    TO_CHAR(FECHA_RECEPCION, 'MM') AS mes,
    TO_CHAR(FECHA_RECEPCION, 'DD') AS dia,
    TO_CHAR(FECHA_RECEPCION, 'Q') AS trimestre
FROM ORDENES_SERVICIO;

